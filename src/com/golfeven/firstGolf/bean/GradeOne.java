package com.golfeven.firstGolf.bean;

public class GradeOne {
	private String hole;
	private String pole;//推杆数
	private String putter;//总杆数
	public String getHole() {
		return hole;
	}
	public void setHole(String hole) {
		this.hole = hole;
	}
	public String getPole() {
		return pole;
	}
	public void setPole(String pole) {
		this.pole = pole;
	}
	public String getPutter() {
		return putter;
	}
	public void setPutter(String putter) {
		this.putter = putter;
	}
	

}
